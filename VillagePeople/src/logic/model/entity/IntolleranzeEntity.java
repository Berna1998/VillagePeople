package logic.model.entity;

public class IntolleranzeEntity {
	String codiceCliente;
	String intolleranza;
	int numeroMenu;
	
	public String getCodiceCliente() {
		return codiceCliente;
	}
	public void setCodiceCliente(String codiceCliente) {
		this.codiceCliente = codiceCliente;
	}
	public String getIntolleranza() {
		return intolleranza;
	}
	public void setIntolleranza(String intolleranza) {
		this.intolleranza = intolleranza;
	}
	public int getNumeroMenu() {
		return numeroMenu;
	}
	public void setNumeroMenu(int numeroMenu) {
		this.numeroMenu = numeroMenu;
	}
	
	

}
